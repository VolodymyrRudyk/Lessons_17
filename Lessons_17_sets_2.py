S = set()                          #ініціалізація пустої множності
S.add(1.23)
print(S)

S.add((1, 2, 3))
print(S)                           #списки і словники не допускаються, но кортежі дозволені

print(S | {(4, 5, 6), (1, 2, 3)})  #об'єднання, те ж саме що і S.union(...)
print((1, 2, 3) in S)              #членство: по повним значенням
print((1, 4, 3) in S)